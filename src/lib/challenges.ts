// ==================== الربط بـ UmmahAPI ====================
const API_URL = "https://ummahapi.com/api/duas/random";

async function fetchFromAPI(): Promise<DhikrChallenge[]> {
  try {
    const response = await fetch(`${API_URL}?count=10`);
    const data = await response.json();

    return data.map((item: any, index: number) => ({
      id: `api-${Date.now()}-${index}`,
      type: "tasbih",
      title: item.dua?.arabic?.substring(0, 40) || "ذكر اليوم",
      target: 3,
      phrases: [item.dua?.arabic || "سبحان الله"],
      virtue: item.reference || "ذكر من الأذكار المأثورة",
      source: item.reference || "UmmahAPI",
    }));
  } catch (e) {
    console.error("فشل جلب الأذكار من API:", e);
    return [];
  }
}

// ==================== إدارة المكتبة الديناميكية ====================
let dynamicLibrary: DhikrChallenge[] = [...localLibrary];

export async function refreshLibrary(): Promise<void> {
  const apiChallenges = await fetchFromAPI();

  if (apiChallenges.length > 0) {
    const existingIds = new Set(dynamicLibrary.map((c) => c.id));
    const newChallenges = apiChallenges.filter((c) => !existingIds.has(c.id));
    dynamicLibrary = [...dynamicLibrary, ...newChallenges];
  }
}

if (typeof window !== "undefined") {
  refreshLibrary();
}

// ==================== دوال مساعدة ====================
export function getRandomChallenge(
  completedIds: string[],
  type?: "tasbih" | "istighfar" | "salawat" | "hawqala" | "tadabbur"
): DhikrChallenge | null {
  const available = dynamicLibrary.filter(
    (c) => !completedIds.includes(c.id) && (!type || c.type === type)
  );

  if (available.length === 0) return null;

  return available[Math.floor(Math.random() * available.length)];
}

export function getTreeStage(completedCount: number): {
  leaves: number;
  branches: number;
  flowers: number;
  fruits: number;
  stage: string;
  nextStage: string;
} {
  const leaves = completedCount;
  const branches = Math.floor(completedCount / 10);
  const flowers = Math.floor(completedCount / 30);
  const fruits = Math.floor(completedCount / 90);

  let stage = "بذرة";
  let nextStage = "ورقة";

  if (leaves >= 1) {
    stage = "ورقة";
    nextStage = "غصن";
  }

  if (branches >= 1) {
    stage = "غصن";
    nextStage = "شجرة صغيرة";
  }

  if (branches >= 5) {
    stage = "شجرة صغيرة";
    nextStage = "شجرة كبيرة";
  }

  if (flowers >= 1) {
    stage = "شجرة كبيرة";
    nextStage = "شجرة مثمرة";
  }

  if (fruits >= 1) {
    stage = "شجرة مثمرة";
    nextStage = "شجرة عملاقة";
  }

  if (fruits >= 5) {
    stage = "شجرة عملاقة";
    nextStage = "شجرة جديدة";
  }

  return { leaves, branches, flowers, fruits, stage, nextStage };
}
