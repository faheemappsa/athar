const appendUnique = (target: DhikrItem[], additions: DhikrItem[]) => {
  const existingIds = new Set(target.map((item) => item.id));
  additions.forEach((item) => {
    if (!existingIds.has(item.id)) {
      target.push(item);
      existingIds.add(item.id);
    }
  });
};

