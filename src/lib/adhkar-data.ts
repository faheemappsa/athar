// قاعدة بيانات الأذكار (مصدر واحد موثوق)
export interface DhikrItem {
  id: string;
  text: string;
  category: "صباح" | "مساء" | "نوم";
  reference: string;
  count?: string;      // نصي مثل "مرة واحدة"، "٣ مرات"
  countNumber?: number; // العدد الرقمي للتكرار (مستخرج من count)
  virtue?: string;
}

// المساعدة: استخراج العدد الرقمي من النص (مثل "٣ مرات" → 3)
function extractNumber(countStr?: string): number | undefined {
  if (!countStr) return undefined;
  const match = countStr.match(/(\d+)/);
  if (match) return parseInt(match[0], 10);
  return undefined;
}

