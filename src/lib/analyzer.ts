// دالة تحليل النص محلياً
export function analyzeLocally(text: string): EmotionResult[] {
  const results: Record<string, number> = {};
  const words = text.split(/\s+/);
  let totalMatches = 0;

  // فحص كل كلمة في القاموس
  words.forEach((word) => {
    for (const [emotion, keywords] of Object.entries(keywordLibrary)) {
      if (keywords.includes(word)) {
        results[emotion] = (results[emotion] || 0) + 1;
        totalMatches++;
      }
    }
  });

  // إذا لم نجد أي تطابق
  if (totalMatches === 0) {
    return [{ emotion: "default", confidence: 1 }];
  }

  // تحويل النتائج إلى نسب مئوية
  const emotionResults: EmotionResult[] = Object.entries(results).map(
    ([emotion, count]) => ({
      emotion,
      confidence: count / totalMatches,
    })
  );

  // ترتيب النتائج من الأعلى ثقة إلى الأقل
  return emotionResults.sort((a, b) => b.confidence - a.confidence);
}

// دالة استخراج المشاعر الرئيسية من نتيجة التحليل
export function getMainEmotions(emotions: EmotionResult[]): string[] {
  // نأخذ المشاعر التي ثقتها أعلى من 0.2 أو أعلى 3 مشاعر
  return emotions
    .filter((e) => e.confidence > 0.2)
    .map((e) => e.emotion)
    .slice(0, 3);
}
