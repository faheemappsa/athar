// قاعدة بيانات الأذكار الكاملة (صالحة للتسبيح التفاعلي)
export interface DhikrItem {
  id: string;
  text: string;
  category: "صباح" | "مساء" | "نوم";
  reference: string;
  countText: string;      // نص العدد مثل "مرة واحدة"، "٣ مرات"
  countNumber: number;    // العدد الرقمي (1، 3، 7، إلخ)
  virtue?: string;
}

// دالة لاستخراج العدد الرقمي من النص
function parseCount(countStr: string): number {
  const match = countStr.match(/(\d+)/);
  if (match) return parseInt(match[0], 10);
  if (countStr.includes("واحدة") || countStr.includes("مرة")) return 1;
  return 1; // افتراضي
}

