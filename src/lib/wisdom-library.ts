// دالة مساعدة لاختيار حكمة عشوائية من الفئة المناسبة
export function getRandomWisdom(emotions: string[]): Wisdom {
  // نجمع الحكم من كل المشاعر المعطاة
  const allWisdom: Wisdom[] = [];
  emotions.forEach((emotion) => {
    const wisdomList = wisdomLibrary[emotion] || wisdomLibrary["default"];
    allWisdom.push(...wisdomList);
  });

  // إذا لم نجد شيئاً، نستخدم الافتراضي
  if (allWisdom.length === 0) {
    const defaults = wisdomLibrary["default"];
    return defaults[Math.floor(Math.random() * defaults.length)];
  }

  // نختار عشوائياً
  return allWisdom[Math.floor(Math.random() * allWisdom.length)];
}
