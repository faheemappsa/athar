// دوال مساعدة
export function getAdhkarByCategory(category: "صباح" | "مساء" | "نوم"): DhikrItem[] {
  return adhkarLibrary.filter(item => item.category === category);
}

export function getAdhkarByTimeOfDay(hour: number): "صباح" | "مساء" | "نوم" {
  if (hour >= 3 && hour < 12) return "صباح";
  if (hour >= 12 && hour < 19) return "مساء";
  return "نوم";
}
